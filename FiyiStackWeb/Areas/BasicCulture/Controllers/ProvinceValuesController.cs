        [HttpPost("~/api/BasicCulture/Province/1/InsertOrUpdateAsync")]
        public async Task<IActionResult> InsertOrUpdateAsync()
        {
            try
            {
                //Get UserId from Session
                int UserId = HttpContext.Session.GetInt32("UserId") ?? 0;

                if(UserId == 0)
                {
                    return StatusCode(401, "User not found in session");
                }

                //Add or edit value
                string AddOrEdit = HttpContext.Request.Form["basicculture-province-title-page"];

                string Name = HttpContext.Request.Form["basicculture-province-name-input"];
                string GeographicalCoordinates = HttpContext.Request.Form["basicculture-province-geographicalcoordinates-input"];
                string Code = HttpContext.Request.Form["basicculture-province-code-input"];
                int CountryId = 0; 
                if (Convert.ToInt32(HttpContext.Request.Form["basicculture-province-countryid-input"]) != 0)
                {
                    CountryId = Convert.ToInt32(HttpContext.Request.Form["basicculture-province-countryid-input"]);
                }
                else
                { return StatusCode(400, "It's not allowed to save zero values in CountryId"); }
                

                ProvinceModel ProvinceModel = new ProvinceModel()
                {
                    Name = Name,
                    GeographicalCoordinates = GeographicalCoordinates,
                    Code = Code,
                    CountryId = CountryId,
                    
                };

                int NewEnteredId = 0;
                int RowsAffected = 0;

                ProvinceModel.DateTimeLastModification = DateTime.Now;
                ProvinceModel.UserLastModificationId = UserId;
                if (AddOrEdit.StartsWith("Add"))
                {
                    ProvinceModel.DateTimeCreation = DateTime.Now;
                    ProvinceModel.UserCreationId = UserId;
                    NewEnteredId = _ProvinceProtocol.Insert(ProvinceModel);
                }
                else
                {
                    int ProvinceId = Convert.ToInt32(HttpContext.Request.Form["basicculture-province-provinceid-input"]);
                    ProvinceModel.ProvinceId = ProvinceId;
                    RowsAffected = _ProvinceProtocol.UpdateByProvinceId(ProvinceModel);
                }
                

                //Look for sent files
                if (HttpContext.Request.Form.Files.Count != 0)
                {
                    int i = 0; //Used to iterate in HttpContext.Request.Form.Files
                    foreach (var File in Request.Form.Files)
                    {
                        if (File.Length > 0)
                        {
                            var FileName = HttpContext.Request.Form.Files[i].FileName;
                            var FilePath = $@"{_WebHostEnvironment.WebRootPath}/Uploads/BasicCulture/Province/";

                            using (var FileStream = new FileStream($@"{FilePath}{FileName}", FileMode.Create))
                            {
                                
                                await File.CopyToAsync(FileStream); // Read file to stream
                                byte[] array = new byte[FileStream.Length]; // Stream to byte array
                                FileStream.Seek(0, SeekOrigin.Begin);
                                FileStream.Read(array, 0, array.Length);
                            }

                            i += 1;
                        }
                    }
                }

                if (AddOrEdit.StartsWith("Add"))
                {
                    return StatusCode(200, NewEnteredId); 
                }
                else
                {
                    return StatusCode(200, RowsAffected);
                }
            }
            catch (Exception ex) 
            { 
                DateTime Now = DateTime.Now;
                FailureModel FailureModel = new FailureModel()
                {
                    HTTPCode = 500,
                    Message = ex.Message,
                    EmergencyLevel = 1,
                    StackTrace = ex.StackTrace ?? "",
                    Source = ex.Source ?? "",
                    Comment = "",
                    Active = true,
                    UserCreationId = 1,
                    UserLastModificationId = 1,
                    DateTimeCreation = Now,
                    DateTimeLastModification = Now
                };
                FailureModel.Insert();
                return StatusCode(500, ex.Message);
            }
        }

