        [HttpPost("~/api/BasicCore/Failure/1/InsertOrUpdateAsync")]
        public async Task<IActionResult> InsertOrUpdateAsync()
        {
            try
            {
                //Get UserId from Session
                int UserId = HttpContext.Session.GetInt32("UserId") ?? 0;

                if(UserId == 0)
                {
                    return StatusCode(401, "User not found in session");
                }

                //Add or edit value
                string AddOrEdit = HttpContext.Request.Form["basiccore-failure-title-page"];

                int HTTPCode = Convert.ToInt32(HttpContext.Request.Form["basiccore-failure-httpcode-input"]);
                int EmergencyLevel = Convert.ToInt32(HttpContext.Request.Form["basiccore-failure-emergencylevel-input"]);
                string Message = HttpContext.Request.Form["basiccore-failure-message-input"];
                string StackTrace = HttpContext.Request.Form["basiccore-failure-stacktrace-input"];
                string Source = HttpContext.Request.Form["basiccore-failure-source-input"];
                string Comment = HttpContext.Request.Form["basiccore-failure-comment-input"];
                

                FailureModel FailureModel = new FailureModel()
                {
                    HTTPCode = HTTPCode,
                    EmergencyLevel = EmergencyLevel,
                    Message = Message,
                    StackTrace = StackTrace,
                    Source = Source,
                    Comment = Comment,
                    
                };

                int NewEnteredId = 0;
                int RowsAffected = 0;

                FailureModel.DateTimeLastModification = DateTime.Now;
                FailureModel.UserLastModificationId = UserId;
                if (AddOrEdit.StartsWith("Add"))
                {
                    FailureModel.DateTimeCreation = DateTime.Now;
                    FailureModel.UserCreationId = UserId;
                    NewEnteredId = _FailureProtocol.Insert(FailureModel);
                }
                else
                {
                    int FailureId = Convert.ToInt32(HttpContext.Request.Form["basiccore-failure-failureid-input"]);
                    FailureModel.FailureId = FailureId;
                    RowsAffected = _FailureProtocol.UpdateByFailureId(FailureModel);
                }
                

                //Look for sent files
                if (HttpContext.Request.Form.Files.Count != 0)
                {
                    int i = 0; //Used to iterate in HttpContext.Request.Form.Files
                    foreach (var File in Request.Form.Files)
                    {
                        if (File.Length > 0)
                        {
                            var FileName = HttpContext.Request.Form.Files[i].FileName;
                            var FilePath = $@"{_WebHostEnvironment.WebRootPath}/Uploads/BasicCore/Failure/";

                            using (var FileStream = new FileStream($@"{FilePath}{FileName}", FileMode.Create))
                            {
                                
                                await File.CopyToAsync(FileStream); // Read file to stream
                                byte[] array = new byte[FileStream.Length]; // Stream to byte array
                                FileStream.Seek(0, SeekOrigin.Begin);
                                FileStream.Read(array, 0, array.Length);
                            }

                            i += 1;
                        }
                    }
                }

                if (AddOrEdit.StartsWith("Add"))
                {
                    return StatusCode(200, NewEnteredId); 
                }
                else
                {
                    return StatusCode(200, RowsAffected);
                }
            }
            catch (Exception ex) 
            { 
                DateTime Now = DateTime.Now;
                FailureModel FailureModel = new FailureModel()
                {
                    HTTPCode = 500,
                    Message = ex.Message,
                    EmergencyLevel = 1,
                    StackTrace = ex.StackTrace ?? "",
                    Source = ex.Source ?? "",
                    Comment = "",
                    Active = true,
                    UserCreationId = 1,
                    UserLastModificationId = 1,
                    DateTimeCreation = Now,
                    DateTimeLastModification = Now
                };
                FailureModel.Insert();
                return StatusCode(500, ex.Message);
            }
        }

